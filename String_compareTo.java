package day21;

public class String_compareTo {

	public static void main(String[] args) {
		String s1="mady";
		String s2="madam";
		if(s1.compareTo(s2)>0)
		{
			System.out.println(s2);
		System.out.println(s1);
		}
		else
		{
			System.out.println(s1);
			System.out.println(s2);
		}

	}

}
